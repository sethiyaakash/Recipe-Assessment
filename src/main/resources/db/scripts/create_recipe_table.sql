CREATE TABLE RECIPE
(
    ID  NUMBER(20) NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR2(50 CHAR) NOT NULL,
    TYPE VARCHAR2(50 CHAR) NOT NULL,
    SERVING_CAPACITY NUMBER(3),
    CREATION_DATE DATE,
    INSTRUCTION VARCHAR2(1000 CHAR) NOT NULL
);

CREATE TABLE INGREDIENT
(
    ID NUMBER(10) NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR2(50 CHAR) NOT NULL,
    QUANTITY VARCHAR(10 CHAR) NOT NULL,
    RECIPE_ID NUMBER(10) NOT NULL
);

